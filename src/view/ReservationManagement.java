    private void addRowTableData() {
        tableModel.setRowCount(0);
         ReservationDao dao = new ReservationDao();
        List<Reservation> Reservations = dao.allReservations();
        for(Reservation user : Reservations){
            tableModel.addRow(new Object[]{
                user.getId(),
                user.getFirstname(),
                user.getLastname(),
                user.getPhone(),
                user.getRoomprice(),
                user.getAddress()
            });
        }}
