    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(idText.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "All Field are required");
        }else if(idText.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(this, " id is Mandator","Field Mandatory",JOptionPane.WARNING_MESSAGE);
        }
        if (!Character.isUpperCase(firstnameText.getText().charAt(0))) {
    JOptionPane.showMessageDialog(this, "First name should start with a capital letter", "Error", JOptionPane.ERROR_MESSAGE);
    return;
}
         else if (!phoneText.getText().matches("\\d{10}")) {
        JOptionPane.showMessageDialog(this, "Phone Number should be 10 digits", "Invalid Phone Number", JOptionPane.WARNING_MESSAGE);
          }
         
          double price = Double.parseDouble(roompriceText.getText().trim());

    if (price <= 25000) {
        JOptionPane.showMessageDialog(this, "Price must be greater than 25000", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }
         
        else{
            // 2. check book id if has 3 characters
            if(idText.getText().length() ==3){
                // call the Model
                Reservation user = new Reservation();
                user.setId(Integer.parseInt(idText.getText()));
                user.setFirstname(firstnameText.getText());
                user.setLastname(lastnameText.getText());
                user.setPhone(phoneText.getText());
                user.setRoomprice(roompriceText.getText());
                user.setAddress(addressText.getText());
                // call dao
                ReservationDao dao = new ReservationDao();
                String feedBack = dao.createReservation(user);
                JOptionPane.showMessageDialog(this, feedBack);
                 addRowTableData();
            }else{
                JOptionPane.showMessageDialog(this, " Id should have min \n and max 3 characters","Book ID Length",JOptionPane.WARNING_MESSAGE);
            }
            
        }       
        

    }//GEN-LAST:event_jButton1ActionPerformed

